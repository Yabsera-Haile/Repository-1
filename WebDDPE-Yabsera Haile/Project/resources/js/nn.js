const food = {};
//localStorage.setItem("foodcount" ,0);
var c = Number(localStorage.getItem("foodcount"));
function add1()
{
    var c = Number(localStorage.getItem("foodcount"));
   
    var f1 = document.getElementById("f1").innerHTML;
    var p1 = document.getElementById("p1").innerHTML;
    food[c] = [f1,p1];
    localStorage.setItem("food",JSON.stringify(food));

  

    c = Number((localStorage.getItem("foodcount")));
    localStorage.removeItem("foodcount");
    
    localStorage.setItem("foodcount",c+1);
    c= Number(localStorage.getItem("foodcount"));
  
    
}

